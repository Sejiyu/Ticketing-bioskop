package Model;
public class Film {
    private String idFilm;
    private String judul;
    private String genre;
    private String durasi;
    private String sinopsis;

    public Film(String idFilm, String judul, String genre, String durasi, String sinopsis) {
        this.idFilm = idFilm;
        this.judul = judul;
        this.genre = genre;
        this.durasi = durasi;
        this.sinopsis = sinopsis;
    }
    
    
}
